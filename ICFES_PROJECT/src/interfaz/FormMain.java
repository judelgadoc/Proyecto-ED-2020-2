    public void cargarTabla(LinkedList titulos){
        DefaultTableModel modelo = new DefaultTableModel();
        jTable1.setModel(modelo);
        for (int i=0 ;i<titulos.size();i++){
                modelo.addColumn((String)titulos.get(i));
        }
    }
    
    
    
    
